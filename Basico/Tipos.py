# Tipos de dados primitivos em Python

'''# Inteiros'''
a = 1
b = 2
c = -7
d = -1000
print(a, b, c, d)

idade = 37
quantidade_itens = 100
ano_nascimento = 1987
saldo_negativo = -1500
print(f"Idade: {idade}, Itens: {quantidade_itens}, Ano de Nascimento: {ano_nascimento}, Saldo Negativo: {saldo_negativo}")

'''# Flutuantes'''
preco_produto = 19.99
altura = 1.75
pi = 3.14159
temperatura = -10.5
print(f"Preço do Produto: {preco_produto}, Altura: {altura}, Pi: {pi}, Temperatura: {temperatura}")

'''# Strings'''
