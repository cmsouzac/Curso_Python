# Description: Concatenacao de strings

# 1. Concatenação é um processo de juntar duas ou mais strings

# Concatenação de strings
string1 = 'Olá'
string2 = 'Mundo'
resultado = string1 + ' ' + string2
print(resultado)

# Concatenação de Listas
lista1 = [1, 2, 3]
lista2 = [4, 5, 6]
resultado = lista1 + lista2
print(resultado)

# Repetição de strings
string = 'Python'
resultado = string * 3
print(resultado)

# Repeticao de listas
lista = [1, 2]
resultado = lista * 3
print(resultado)

# Combinando repetição e concatenação
texto = "Oi" + " " + "Python" * 2
print(texto)